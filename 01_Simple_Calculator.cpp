// Calculator Program
#include <iostream>
using namespace std;


int main() {
	int a, b;
	
	cout << "Input a number =>";
	cin >> a;
	cout << "Input another number =>";
	cin >> b;
	
	cout <<a<<" + "<<b<<" = " << a+b << endl;
	cout <<a<<" - "<<b<<" = " << a-b << endl;
	cout <<a<<" * "<<b<<" = " << a*b << endl;
	cout <<a<<" / "<<b<<" = " << a/b << endl;
	cout <<a<<" % "<<b<<" = " << a%b << endl;
}


